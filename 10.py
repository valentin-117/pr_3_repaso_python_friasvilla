#se crea la lista
lista = ["manzana", "banana", "cereza", "kiwi", "uva"]

# iniciamos un índice para recorrer la lista
indice = 0

# Usamos un bucle while para recorrer e imprimir todos los elementos de la lista
while indice < len(lista):
    print(lista[indice])
    indice += 1  # Aumentamos el índice para acceder al siguiente elemento
